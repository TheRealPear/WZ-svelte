  <h2 class="text-2xl">Privacy Statement</h2>
  <p>Player privacy is prioritised during a screenshare. Staff members conducting screenshares will not look for any information that does not pertain to cheat detection. That being said, Warzone does not guarantee sensitive information will not be encountered in the screenshare process. Players are expected to make sure that sensitive information is not stored in any folders that are related to Minecraft or similar. In the event that sensitive information is discovered, it will be kept private within the screenshare & senior staff.</p>
  <p>If a player requires any clarification about programs or methods during a screenshare, the screenshare will be paused and questions will be answered. Players may request to pause or stop the screenshare at any time.</p>
  <p>Note that all screenshares are recorded to be reviewed by senior staff members for quality assurance and to be referenced in the event of an abuse report.</p>
  <div role="alert" class="not-prose alert alert-soft alert-error">
    <ExclamationTriangle />
    <span class="font-bold">If you feel like your privacy has been violated in a Warzone screenshare, or have any complaints about a screenshare, please contact a senior staff member immediately.</span>
  </div>
  <h2 class="text-2xl">Process</h2>
  <ol>
    <li>A suspicious player will be asked to submit to a screenshare by a Warzone staff member.</li>
    <li>The player will join a public voice channel in the Warzone Discord server, to be moved into a private voice channel with staff.</li>
    <li>Once the player and screenshare staff are in the voice channel, the player will be asked to share their entire screen on Discord.</li>
    <li>The player will be asked to install AnyDesk, a program that provides remote desktop access to the staff member performing the screenshare.</li>
    <li>During the screenshare, the screenshare staff may install trusted programs listed on this page that aid in the cheat detection process.</li>
    <li>Once the Warzone staff members have come to a conclusion, the screenshare will end, and further information will be given by the screenshare staff.</li>
  </ol>
  <h2 class="text-2xl">Failure</h2>
  <p>Doing or being caught with any of the following during a screenshare without staff approval will result in an automatic screenshare failure and consequently a ban from the server:</p>
  <ul>
    <li>Using a FAT filesystem.</li>
    <li>Using VeraCrypt or other disk encryption programs.</li>
    <li>Found traces of clients or autoclickers used before or while playing on Warzone.</li>
    <li>Having Windows or equivalent operating system services disabled.</li>
    <li>Failing to join the Discord voice channel within five minutes of being asked.</li>
    <li>Logging out of the server after being asked to screenshare (up to staff discretion).</li>
    <li>Clearing or modifying your Recycle Bin/Trash folder immediately before a screenshare.</li>
    <li>Deleting, modifying, or moving files that screenshare staff deem suspicious.</li>
    <li>Configuring antivirus or firewall settings.</li>
    <li>Opening or closing programs.</li>
    <li>Configuring game settings.</li>
    <li>Configuring controls, key bindings, or peripherals (e.g. mouse sensitivity).</li>
    <li>Connecting or disconnecting external devices.</li>
    <li>Stalling the screenshare or failing to comply with instructions from the screenshare staff.</li>
    <li>Restarting Windows or equivalent operating system services or processes in an attempt to obstruct the screenshare.</li>
    <li>Using applications that interfere with screenshare tools.</li>
    <li>Recording the screenshare (after one warning from staff); passive recording programs such as NVIDIA ShadowPlay are also not allowed.</li>
    <li>Leaving the screenshare.</li>
    <li>Admitting to cheating.</li>
  </ul>
  <p>Sharing information about the screenshare process that is not publicly available on this page at any point may result in a punishment.</p>
  <h2 class="text-2xl">Who We Screenshare</h2>
  <p>Warzone staff do not conduct screenshares simply at a player's request. However, players who have previously been screenshared may be subject to another screenshare in the future. If a player is reported and there is not enough evidence to warrant a punishment, staff may initiate a screenshare based on suspicion. Ultimately, the decision to screenshare any player rests solely with the Warzone staff team, who exercise full discretion in determining when and to whom this process applies.</p>
